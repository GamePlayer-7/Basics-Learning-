string = 'Strings in Python'
print(string)            # Prints complete string
print(string[0])         # Prints first character of the string
print(string[5:13])      # Prints characters starting from 3rd to 5th
print(string[3:])        # Prints string starting from 4th character
print(string[:7])        # Prints string starting from Begning till 6th character
print(string * 2)        # Prints string two times
print(string + "Easy")   # Prints concatenated string

